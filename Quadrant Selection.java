import java.util.*;
public class Quad{
    public static void main(String args[]){
        Scanner nuts=new Scanner(System.in);
        int x=nuts.nextInt();
        int y=nuts.nextInt();
        if(x<0&&y<0){
            System.out.println("3");
        }
        else if(x<0&&y>0){
            System.out.println("2");
        }
        else if(x>0&&y<0){
            System.out.println("4");
        }
        else{
            System.out.println("1");
        }
    }
}
